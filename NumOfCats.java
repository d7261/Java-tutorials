public class NumOfCats {
  public static void main(String args[]){
    int numOfCats = 84;
    numOfCats = numOfCats+10;

    System.out.println("The number of cats after addding = " + numOfCats);

  
    numOfCats--;

    System.out.println("The cats remaining are:" +numOfCats);

  }
}
